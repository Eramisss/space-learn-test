@transaction.atomic
def seed_starter_decks(user):
    """
    Создаёт стартовые колоды и карточки для указанного пользователя.
    Вызывается один раз — сразу после регистрации.
    """
    for deck_data in STARTER_DECKS:
        deck = Deck.objects.create(
            owner=user,
            title=deck_data['title'],
            description=deck_data['description'],
            is_public=False,
        )
        Card.objects.bulk_create([
            Card(deck=deck, front=front, back=back)
            for front, back in deck_data['cards']
        ])
